def addition():
    a = float(input("Augend: "))
    b = float(input("Addend: "))
    c = float(a+b)

    a = str(a)
    b = str(b)
    c = str(c)

    print(f"{a} + {b} = {c}")

def subtraction():
    a = float(input("Subtrahend: "))
    b = float(input("Subtractor: "))
    c = float(a-b)

    a = str(a)
    b = str(b)
    c = str(c)

    print(f"{a} - {b} = {c}")
